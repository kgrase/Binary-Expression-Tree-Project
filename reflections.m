# Project Reflection

## 1. What did you find most challenging with this program?
The most challenging part was implementing the recursive evaluation of the expression tree, ensuring proper handling of operators and operands while managing memory efficiently.

## 2. What problems did you encounter and how did you solve them?
I faced issues parsing the postfix expression and handling the stack during tree construction. I solved this by implementing proper stack operations, ensuring correct node connections, and handling edge cases like empty stacks.

## 3. What did you learn from writing this program?
I learned how to use a binary expression tree to evaluate postfix expressions and gained experience in recursion, stack operations, and memory management.
